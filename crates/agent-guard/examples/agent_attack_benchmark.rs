//! Agent-attack proof harness (spec 079 P1).
//!
//! Runs the curated agent-attack + benign corpus through the real embedded ATR
//! engine (`analyze_command`, the `/api/agent/check-command` path) and writes an
//! honest `SCOREBOARD.md` (catch rate, hard-deny rate, false-positive rate,
//! per-category breakdown, and the explicit list of MISSES).
//!
//! Run:
//!   cargo run -p innerwarden-agent-guard --example agent_attack_benchmark
//!
//! The misses are the point: they define the spec 079 P2/P3 backlog.

use std::collections::BTreeMap;
use std::fmt::Write as _;

use innerwarden_agent_guard::benchmark::{run, Corpus, Outcome, Scoreboard};
use innerwarden_agent_guard::rules::RuleEngine;

const CORPUS_YAML: &str = include_str!("../benchmarks/agent_attack_corpus.yml");

fn main() {
    let corpus = Corpus::from_yaml(CORPUS_YAML).expect("corpus must parse");
    let engine = RuleEngine::load_embedded();
    let results = run(&corpus, &engine);
    let board = Scoreboard::from_results(&results);

    // Per-category catch rate (malicious only) + per-category FP (benign only).
    let mut cat: BTreeMap<String, (usize, usize)> = BTreeMap::new(); // category -> (caught_or_ok, total)
    for r in &results {
        let e = cat.entry(r.category.clone()).or_insert((0, 0));
        e.1 += 1;
        match r.outcome {
            Outcome::Caught | Outcome::Ok => e.0 += 1,
            _ => {}
        }
    }

    let mut md = String::new();
    let _ = writeln!(md, "# Agent-Guard Proof Scoreboard (spec 079 P1)\n");
    let _ = writeln!(
        md,
        "Honest baseline of `agent_guard::mcp::analyze_command` + the embedded ATR \
         engine against a {}-case corpus. Generated by \
         `cargo run -p innerwarden-agent-guard --example agent_attack_benchmark`.\n",
        corpus.cases.len()
    );

    let _ = writeln!(md, "## Headline\n");
    let _ = writeln!(md, "| Metric | Value |");
    let _ = writeln!(md, "|--------|-------|");
    let _ = writeln!(
        md,
        "| Malicious caught (deny or review) | **{}/{} = {:.1}%** |",
        board.caught,
        board.malicious_total,
        board.catch_rate()
    );
    let _ = writeln!(
        md,
        "| Malicious hard-denied | {}/{} = {:.1}% |",
        board.denied,
        board.malicious_total,
        board.deny_rate()
    );
    let _ = writeln!(
        md,
        "| Malicious MISSED (allowed through) | {}/{} = {:.1}% |",
        board.missed,
        board.malicious_total,
        100.0 - board.catch_rate()
    );
    let _ = writeln!(
        md,
        "| Benign false positives | {}/{} = {:.1}% |",
        board.false_positives,
        board.benign_total,
        board.false_positive_rate()
    );

    let _ = writeln!(md, "\n## Per-category\n");
    let _ = writeln!(md, "| Category | Correct / Total |");
    let _ = writeln!(md, "|----------|-----------------|");
    for (c, (ok, tot)) in &cat {
        let _ = writeln!(md, "| {c} | {ok}/{tot} |");
    }

    // The MISSES — explicit, never hidden.
    let misses: Vec<_> = results
        .iter()
        .filter(|r| r.outcome == Outcome::Missed)
        .collect();
    let _ = writeln!(
        md,
        "\n## Misses (malicious allowed through — the P2/P3 backlog)\n"
    );
    if misses.is_empty() {
        let _ = writeln!(md, "_None._");
    } else {
        let _ = writeln!(md, "| id | category | risk | signals | input |");
        let _ = writeln!(md, "|----|----------|------|---------|-------|");
        for m in &misses {
            let preview: String = m.input.chars().take(60).collect();
            let _ = writeln!(
                md,
                "| {} | {} | {} | {} | `{}` |",
                m.id,
                m.category,
                m.risk_score,
                if m.signals.is_empty() {
                    "—".into()
                } else {
                    m.signals.join(", ")
                },
                preview.replace('|', "\\|")
            );
        }
    }

    // False positives — explicit.
    let fps: Vec<_> = results
        .iter()
        .filter(|r| r.outcome == Outcome::FalsePositive)
        .collect();
    let _ = writeln!(md, "\n## False positives (benign flagged)\n");
    if fps.is_empty() {
        let _ = writeln!(md, "_None._");
    } else {
        let _ = writeln!(
            md,
            "| id | category | rec | risk | atr rules | signals | input |"
        );
        let _ = writeln!(
            md,
            "|----|----------|-----|------|-----------|---------|-------|"
        );
        for f in &fps {
            let preview: String = f.input.chars().take(46).collect();
            let _ = writeln!(
                md,
                "| {} | {} | {} | {} | {} | {} | `{}` |",
                f.id,
                f.category,
                f.recommendation,
                f.risk_score,
                if f.atr_rule_ids.is_empty() {
                    "—".into()
                } else {
                    f.atr_rule_ids.join(", ")
                },
                if f.signals.is_empty() {
                    "—".into()
                } else {
                    f.signals.join(", ")
                },
                preview.replace('|', "\\|")
            );
        }
    }

    let _ = writeln!(
        md,
        "\n---\n_Misses on prompt-injection / indirect-injection / tool-poisoning \
         are expected: the current engine is a command-string analyzer. Closing \
         them is spec 079 P2 (deep MCP inspection) + P3 (ATR expansion). This \
         scoreboard is the measured baseline those phases must beat._"
    );

    // Write next to the corpus.
    let out = concat!(env!("CARGO_MANIFEST_DIR"), "/benchmarks/SCOREBOARD.md");
    std::fs::write(out, &md).expect("write SCOREBOARD.md");

    // Also print the headline to stdout.
    println!("=== Agent-Guard Proof Baseline ===");
    println!(
        "catch (deny|review): {}/{} = {:.1}%   hard-deny: {}/{} = {:.1}%   miss: {}   FP: {}/{} = {:.1}%",
        board.caught, board.malicious_total, board.catch_rate(),
        board.denied, board.malicious_total, board.deny_rate(),
        board.missed,
        board.false_positives, board.benign_total, board.false_positive_rate()
    );
    println!("SCOREBOARD.md written to {out}");
    if !misses.is_empty() {
        println!("\nMISSES ({}):", misses.len());
        for m in &misses {
            println!("  [{}] {} (risk {})", m.category, m.id, m.risk_score);
        }
    }
    if !fps.is_empty() {
        println!("\nFALSE POSITIVES ({}):", fps.len());
        for f in &fps {
            println!(
                "  [{}] {} ({}, risk {}) signals: {} | rules: {}",
                f.category,
                f.id,
                f.recommendation,
                f.risk_score,
                if f.signals.is_empty() {
                    "—".into()
                } else {
                    f.signals.join(", ")
                },
                if f.atr_rule_ids.is_empty() {
                    "—".into()
                } else {
                    f.atr_rule_ids.join(", ")
                }
            );
        }
    }
}
